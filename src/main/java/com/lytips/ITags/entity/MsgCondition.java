package com.lytips.ITags.entity;

public class MsgCondition {
	private Integer msgId;
	private Boolean upStoreCount;
	private Boolean upTransferCount;
	private Boolean upCommentCount;
	private Boolean upGoodCount;
	
	
	
	public Integer getMsgId() {
		return msgId;
	}
	public void setMsgId(Integer msgId) {
		this.msgId = msgId;
	}
	public Boolean getUpStoreCount() {
		return upStoreCount;
	}
	public void setUpStoreCount(Boolean upStoreCount) {
		this.upStoreCount = upStoreCount;
	}
	public Boolean getUpTransferCount() {
		return upTransferCount;
	}
	public void setUpTransferCount(Boolean upTransferCount) {
		this.upTransferCount = upTransferCount;
	}
	public Boolean getUpCommentCount() {
		return upCommentCount;
	}
	public void setUpCommentCount(Boolean upCommentCount) {
		this.upCommentCount = upCommentCount;
	}
	public Boolean getUpGoodCount() {
		return upGoodCount;
	}
	public void setUpGoodCount(Boolean upGoodCount) {
		this.upGoodCount = upGoodCount;
	}
	
	
}
